package Operaciones;

public class Resta {

    public static void restar(int num1, int num2) {
        int restar = num1 - num2;
        System.out.println(restar);
    }

    public static void restar(double num1, double num2) {
        double restar = num1 - num2;
        System.out.println(restar);
    }
}
