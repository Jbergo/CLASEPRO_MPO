package Operaciones;

public class Dividir {

    public static void dividir(int num1, int num2) {
        int dividir = num1 / num2;
        System.out.println(dividir);
    }

    public static void dividir(double num1, double num2) {
        double dividir = num1 / num2;
        System.out.println(dividir);
    }
}
