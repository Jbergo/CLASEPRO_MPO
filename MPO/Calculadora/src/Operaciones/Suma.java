package Operaciones;

public class Suma {

    public static void sumar(int num1, int num2) {
        int sumar = num1 + num2;
        System.out.println(sumar);
    }

    public static void sumar(double num1, double num2) {
        double sumar = num1 + num2;
        System.out.println(sumar);
    }

}
